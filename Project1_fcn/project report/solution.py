import math

def sum(num1, num2):
	sum = int(num1) + int(num2)    
	return str(sum)

def sub(num1, num2):
	sub = int(num1) - int(num2)
	return str(sub)

def mul(num1, num2):
	mul = int(num1) * int(num2)
	return str(mul)

def div(num1, num2):
	div = int(num1)/int(num2)
	return str(div)
